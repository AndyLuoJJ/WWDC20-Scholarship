 # Queue

 Similar to stacks, queues also appear frequently in our daily life. The most obvious scenario is when you are waiting in a queue at the checkout counter when you finish shopping. The cashier helps the **first** customer in the queue to check out. When another customer completes shopping, he or she would join at the **end** of the queue.
 
 From the above scenario, we discover that insertion happens at one end of a queue, while removal happens at the other end. Insertion and removal of a queue are known as *enqueue* and *dequeue* respectively.

