# Stack

 Actually, stacks appear in our daily life frequently. Thinking about a pile of papers used by a printing machine. When the machine starts printing, it takes the paper at **top**. When it runs out of paper and you need to add more papers, the most convenient way is to add at the **top** of that pile of papers.
 
 That pile of paper can be regarded as a stack. This means that you can only inquire, insert or remove at one end of the stack. Inserting, removing and inquiring from a stack is called *pushing*, *popping* and *topping*. All these manipulations are extremely fast.

 - Experiment:
 Run this playground page and interact with the live view to see how `Stack` works. Graphics are better than words!
